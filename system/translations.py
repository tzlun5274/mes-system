def get_permissions_text(permissions):
    """
    將權限列表轉換為中文文字描述
    
    Args:
        permissions: 權限查詢集
        
    Returns:
        str: 權限的中文描述文字
    """
    permission_texts = []
    for perm in permissions:
        codename = perm.codename
        
        # 直接查詢對應表
        if codename in PERMISSION_NAME_TRANSLATIONS:
            permission_texts.append(PERMISSION_NAME_TRANSLATIONS[codename])
        else:
            # 如果沒有對應，顯示原始名稱
            permission_texts.append(codename)
    
    # 去重並限制顯示數量
    unique_permissions = list(dict.fromkeys(permission_texts))
    return "、".join(unique_permissions[:8]) + ("..." if len(unique_permissions) > 8 else "")


